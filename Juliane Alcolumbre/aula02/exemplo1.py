#Operadores Aritiméticos (Conitinuação)
valor1  = 8
valor2 = 11

print("Exponenciação: ",valor1**3)
print("Divisão padrão: ",valor1/valor2)
print("divisão sem decimais:",valor1//valor2)
print("Módulo(resto) da divisão:",valor2%3)

#Operadores de Comparação (Relacionais)
print("valor1 é maior do que valor2:",valor1>valor2)
print("valor1 é maior ou igual do que valor2:",valor1>=valor2)
print("valor1 é menor do que valor2:",valor1>valor2)
print("valor1 é menor ou igual do que valor2:",valor1>=valor2)
print("valor1 é igual ao valor2:",valor1==valor2)
print("valor1 é diferente ao valor2:",valor1!=valor2)