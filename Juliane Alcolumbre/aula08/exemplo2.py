#Funções com retorno
def soma(v1,v2):
    total=v1+v2
    return total
# print(soma(10,20))

#Função para calcular a média de 3 valores
def media_tres(v1,v2,v3):
    return(v1+v2+v3)/3
# print(media_tres(10,8,9))

#Função para mostrar o maor valor a partir de dois números
def maior (m1,m2):
    return max(m1,m2)
